void lcd_send_cmd(char data){
	uint8_t cmd[2];
	cmd[0] &= 0x00;
	cmd[1] &= 0x00;
	cmd[1] |= data;
	HAL_I2C_Master_Transmit(&hi2c1, slvaddr, cmd, 2, 100);
}

void lcd_send_data(char data){
	uint8_t cmd[2];
	cmd[0] &= 0x00;
	cmd[0] |= 0x40;
	cmd[1] &= 0x00;
	cmd[1] |= data;
	HAL_I2C_Master_Transmit(&hi2c1, slvaddr, cmd, 2, 100);
}

void lcd_clear(){
	lcd_send_cmd(0x01);
}

void lcd_send_string (char *str)
{
	while (*str) lcd_send_data (*str++);
}

void lcd_set_cursor(int row, int col){
	switch(row){
	case 1:
		col |= 0x80;
		break;
	case 2:
		col |= 0xC0;
		break;
	}
	lcd_send_cmd(col);
	col=0;
}

void lcd_Init(){
	HAL_Delay(20);
	lcd_send_cmd(0x20);
	HAL_Delay(1);
	lcd_send_cmd(0x0C);
	HAL_Delay(1);
	lcd_send_cmd(0x01);
	HAL_Delay(1);
	lcd_send_cmd(0x06);
	HAL_Delay(1);
}

void lcd_moving_word(char * str){
	int cnt=0;
	lcd_set_cursor(1,15);
	while(1){
			char pos = *(str+cnt) ;
			lcd_send_data(pos);
		HAL_Delay(350);
		lcd_send_cmd(0x18);
		cnt++;
		if(cnt>=strlen(str)){
			lcd_send_string("    ");
			cnt=0;
		}

		}
}

